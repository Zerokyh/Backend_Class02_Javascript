// 조건 - if/else-if/else
// 조건 - switch
// 위에 두개는 잘 쓰이지 않음

const season = "spring";

switch (season) {
  case "spring":
    console.log("봄");
    break;
  case "summer":
    console.log("여름");
    break;
  case "fall":
    console.log("가을");
    break;
  case "winter":
    console.log("겨울");
    break;
  default:
    console.log("알수없음");
    break;
}
